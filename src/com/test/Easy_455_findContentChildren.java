public class Easy_455_findContentChildren {
	public static void main(String[] args) {
		int[] g = {1,2};
		int[] s = {1,2,3};
		System.out.println(findContentChildren(g, s));
	}
	public static int findContentChildren(int[] g, int[] s) {
        Arrays.sort(g);
        Arrays.sort(s);
        int gi = 0;
        int si = 0;
        while(gi < g.length && si < s.length) {
        	if (g[gi] <= s[si]) {
				gi++;
			}
        	si++;
        }
        return gi;
    }
}
